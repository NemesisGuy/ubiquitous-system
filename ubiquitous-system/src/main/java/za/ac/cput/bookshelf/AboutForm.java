    public void displayLogoBannerCenter(URL selectedFileUrl) {

        Image image = null;
        try {
            image = ImageIO.read(selectedFileUrl);
        } catch (IOException ex) {
            Logger.getLogger(SystemSettingsCompanyProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        ImageIcon imageIcon;
        jLabelPictureBannerCenter.setIcon(imageIcon = new ImageIcon(image));

    }


