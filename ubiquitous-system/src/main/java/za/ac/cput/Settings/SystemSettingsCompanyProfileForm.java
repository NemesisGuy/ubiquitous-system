    public void displayLogo(File selectedFile) throws IOException {
        URL url;
        try {
            System.out.println(selectedFile.getAbsolutePath());
            url = new URL("file:///" + selectedFile.getAbsolutePath());
            Image image = ImageIO.read(url);
            ImageIcon imageIcon;
            jLabelPicture.setIcon(imageIcon = new ImageIcon(image.getScaledInstance(jPanelImagePanel.getWidth(), jPanelImagePanel.getHeight(), Image.SCALE_SMOOTH)));
        } catch (MalformedURLException ex) {
            Logger.getLogger(SystemSettingsCompanyProfileForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

